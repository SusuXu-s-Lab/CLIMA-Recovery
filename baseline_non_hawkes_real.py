def load_real_communities(
    npz_path: str,
    ref_lon: float,
    ref_lat: float,
    max_communities: int,
    min_nodes_real: int,
    max_nodes_real: int,
) -> Tuple[List[dict], Dict, List[int]]:
    """
    Load NPZ from prepare_real_ian_by_cbg.py and select a subset of communities
    using the same selection heuristic as your Graph Hawkes pipeline.

    Returns:
        communities: list of dicts
        meta: meta dict
        selected_indices: indices into the original communities array
    """
    data = np.load(npz_path, allow_pickle=True)
    communities_all = list(data["communities"])
    meta = data["meta"].item()

    selected_indices = select_communities_by_distance(
        communities=communities_all,
        ref_lon=ref_lon,
        ref_lat=ref_lat,
        max_communities=max_communities,
        min_nodes=min_nodes_real,
        max_nodes=max_nodes_real,
    )
    communities = [communities_all[i] for i in selected_indices]
    return communities, meta, selected_indices


def build_knn_weights(coords: np.ndarray, k: int = 5) -> np.ndarray:
    """
    Construct a simple kNN spatial weights matrix W [N, N] from coordinates.

    coords: [N, 2] with (lat, lon) or (lon, lat) consistently.
    W_ij = 1/k if j is among i's k nearest neighbors, else 0.
    Rows normalized to sum to 1 (typical spatial lag construction).

    This is commonly used in spatial econometrics and diffusion models.
    """
    N = coords.shape[0]
    # Use Euclidean distance in (lat, lon) for simplicity
    dists = np.sum((coords[:, None, :] - coords[None, :, :]) ** 2, axis=2)  # [N, N]
    np.fill_diagonal(dists, np.inf)

    # indices of k nearest neighbors per i
    knn_idx = np.argpartition(dists, kth=k, axis=1)[:, :k]  # [N, k]

    W = np.zeros((N, N), dtype=np.float32)
    for i in range(N):
        neighbors = knn_idx[i]
        W[i, neighbors] = 1.0 / float(k)

    return W


