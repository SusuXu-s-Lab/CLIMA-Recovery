def build_cox_dataset(communities: List[dict], meta: Dict, event_type: int):
    """
    Build dataset for a Cox model: one row per node, with
      - duration: time to first event of `event_type` (0 or 1)
      - event: 1 if event observed, 0 if censored
      - covariates: static X_i

    event_type: 0 = sell, 1 = repair
    """
    T = int(meta["T"])
    X_rows = []
    durations = []
    events = []

    for comm in communities:
        X_g = comm["X"].astype(np.float32)    # [N_g, d]
        Y_g = comm["Y"].astype(np.float32)    # [T, N_g, K]
        T_g, N_g, _ = Y_g.shape
        assert T_g == T

        for i in range(N_g):
            y_i = Y_g[:, i, event_type]  # [T]
            if y_i.sum() > 0:
                t_event = int(np.argmax(y_i > 0))  # first month index
                event = 1
            else:
                t_event = T - 1
                event = 0

            durations.append(t_event + 1)  # months since start, 1-based
            events.append(event)
            X_rows.append(X_g[i])

    X_mat = np.stack(X_rows, axis=0)
    durations = np.array(durations, dtype=np.float32)
    events = np.array(events, dtype=np.int32)
    return X_mat, durations, events


def train_cox(
    communities: List[dict],
    meta: Dict,
    event_type: int,
    feature_names: List[str] | None = None,
):
    """
    Fit a CoxPH model for sell (event_type=0) or repair (event_type=1)
    using lifelines. This is a node-level time-to-first-event model.

    Returns the fitted CoxPHFitter. You can use it to get survival and
    hazard estimates and then translate them into per-(t,i) probabilities.
    """
    if not HAS_LIFELINES:
        raise RuntimeError("lifelines is not installed; install via `pip install lifelines`.")

    X_mat, durations, events = build_cox_dataset(communities, meta, event_type)
    N, d = X_mat.shape

    if feature_names is None:
        feature_names = [f"x{j}" for j in range(d)]

    df = pd.DataFrame(X_mat, columns=feature_names)
    df["duration"] = durations
    df["event"] = events

    cph = CoxPHFitter()
    cph.fit(df, duration_col="duration", event_col="event", show_progress=True)
    return cph


def cox_predict_logits_per_t(
    cph_sell,
    cph_rep,
    communities: List[dict],
    meta: Dict,
    feature_names: List[str],
) -> Dict[int, np.ndarray]:
    """
    Example: translate CoxPH predictions into per-(t,i,k) logits.

    For each node i, and each time t, we compute approximate probability
    that event happens at t (given it has not happened yet):
        p(t) ≈ S(t-1) - S(t)
    where S(t) is survival probability from CoxPH.

    This produces a logits_dict[g] with shape [T, N_g, 2].
    """
    T = int(meta["T"])
    logits_dict: Dict[int, np.ndarray] = {}

    for g, comm in enumerate(communities):
        X_g = comm["X"].astype(np.float32)
        Y_g = comm["Y"].astype(np.float32)
        T_g, N_g, K = Y_g.shape
        assert T_g == T

        df_X = pd.DataFrame(X_g, columns=feature_names)

        # times 1..T as months
        times = np.arange(1, T + 1)

        # Survival for each node and type
        S_sell = cph_sell.predict_survival_function(df_X, times=times)  # DataFrame: index=times, columns=nodes
        S_rep = cph_rep.predict_survival_function(df_X, times=times)

        logits_g = np.zeros((T, N_g, 2), dtype=np.float32)

        for i in range(N_g):
            # series indexed by time
            S_s_i = S_sell.iloc[:, i].values  # length T
            S_r_i = S_rep.iloc[:, i].values

            for t in range(T):
                S_prev_s = 1.0 if t == 0 else S_s_i[t - 1]
                S_curr_s = S_s_i[t]
                p_s = max(S_prev_s - S_curr_s, 1e-8)

                S_prev_r = 1.0 if t == 0 else S_r_i[t - 1]
                S_curr_r = S_r_i[t]
                p_r = max(S_prev_r - S_curr_r, 1e-8)

                # Convert to logits
                logits_g[t, i, 0] = np.log(p_s) - np.log(1.0 - p_s)
                logits_g[t, i, 1] = np.log(p_r) - np.log(1.0 - p_r)

        logits_dict[g] = logits_g

    return logits_dict


