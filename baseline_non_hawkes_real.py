def run_and_eval_panel_baseline(
    npz_path: str,
    baseline: Literal["rum", "spatial_lag", "recovus"],
    ref_lon: float,
    ref_lat: float,
    max_communities: int,
    min_nodes_real: int,
    max_nodes_real: int,
    num_epochs: int = 20,
    lr: float = 1e-3,
    hidden_dim: int = 0,
    device: str = "cpu",
    horizon_months: int = 6,
):
    """
    Convenience function:
      - load communities
      - build panel data
      - train panel logistic baseline
      - predict logits[g] over full horizon
      - call your existing evaluate_tail_metrics
    """
    communities, meta, _ = load_real_communities(
        npz_path, ref_lon, ref_lat, max_communities, min_nodes_real, max_nodes_real
    )
    T_train = int(meta["T_train"])

    print(f"[INFO] Loaded {len(communities)} communities from {npz_path}")
    print(f"[INFO] Training panel-{baseline} baseline...")

