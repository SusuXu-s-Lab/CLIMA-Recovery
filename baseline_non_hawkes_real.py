# Neural RNN / RMTPP-style baseline (per-node GRU)
# ----------------------------------------------------------------------

class NodeSequenceRNN(nn.Module):
    """
    Simple per-node GRU baseline.

    For each node i:
      input sequence: [T-1, d_in] where d_in = K + d_static
        (past events Y[t-1, i, :] and static features X_i)
      target: Y[t, i, :] for t=1..T_train-1

    This ignores explicit network structure but captures rich
    temporal nonlinearities similar in spirit to RMTPP / Neural Hawkes.
    """

    def __init__(self, d_in: int, hidden_dim: int = 32, K: int = 2):
        super().__init__()
        self.gru = nn.GRU(input_size=d_in, hidden_size=hidden_dim, batch_first=True)
        self.out = nn.Linear(hidden_dim, K)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        """
        x: [B, T-1, d_in]
        returns logits: [B, T-1, K]
        """
        h, _ = self.gru(x)  # [B, T-1, hidden_dim]
        logits = self.out(h)  # [B, T-1, K]
        return logits


def build_rnn_sequences(
    communities: List[dict],
    meta: Dict,
) -> Tuple[np.ndarray, np.ndarray]:
    """
    Build sequences for NodeSequenceRNN.

    Returns:
      X_seq: [B, T-1, d_in]
      Y_seq: [B, T-1, K] (targets)
    where B = total number of nodes across selected communities.
    """
    T = int(meta["T"])
    T_train = int(meta["T_train"])
    K = 2

    X_seq_list = []
    Y_seq_list = []

    for comm in communities:
        X_g = comm["X"].astype(np.float32)    # [N_g, d_static]
        Y_g = comm["Y"].astype(np.float32)    # [T, N_g, K]
        T_g, N_g, _ = Y_g.shape
        assert T_g == T
        d_static = X_g.shape[1]

        # For training, we only use up to T_train
        for i in range(N_g):
            x_i = X_g[i]  # [d_static]
            # inputs: concatenate static X_i to each time step of past events
            past_events = Y_g[:T_train - 1, i, :]  # [T_train-1, K]
            x_rep = np.repeat(x_i[None, :], repeats=past_events.shape[0], axis=0)  # [T_train-1, d_static]
            X_i_seq = np.concatenate([past_events, x_rep], axis=1)  # [T_train-1, K + d_static]

            # targets: next events
            Y_i_seq = Y_g[1:T_train, i, :]  # [T_train-1, K]

            X_seq_list.append(X_i_seq)
            Y_seq_list.append(Y_i_seq)

    X_seq = np.stack(X_seq_list, axis=0)  # [B, T_train-1, d_in]
    Y_seq = np.stack(Y_seq_list, axis=0)  # [B, T_train-1, K]
    return X_seq, Y_seq


def train_rnn_baseline(
    communities: List[dict],
    meta: Dict,
    hidden_dim: int = 32,
    num_epochs: int = 20,
    lr: float = 1e-3,
    device: str = "cpu",
) -> NodeSequenceRNN:
    """
    Train NodeSequenceRNN baseline over all nodes in selected communities.
    """
    T = int(meta["T"])
    T_train = int(meta["T_train"])
    K = 2

    X_seq, Y_seq = build_rnn_sequences(communities, meta)
    B, Tm1, d_in = X_seq.shape

    X_t = torch.from_numpy(X_seq).to(device)
    Y_t = torch.from_numpy(Y_seq).to(device)

    model = NodeSequenceRNN(d_in=d_in, hidden_dim=hidden_dim, K=K).to(device)
    optimizer = torch.optim.Adam(model.parameters(), lr=lr)
    loss_fn = nn.BCEWithLogitsLoss()

    for epoch in range(1, num_epochs + 1):
        model.train()
        optimizer.zero_grad()
        logits = model(X_t)  # [B, T_train-1, K]
        loss = loss_fn(logits, Y_t)
        loss.backward()
        optimizer.step()

        if epoch == 1 or epoch % 5 == 0 or epoch == num_epochs:
            print(f"[RNN baseline epoch {epoch:03d}] loss={loss.item():.4f}")

    return model


def rnn_predict_logits_per_community(
    model: NodeSequenceRNN,
    communities: List[dict],
    meta: Dict,
    device: str = "cpu",
) -> Dict[int, np.ndarray]:
    """
    Use a trained NodeSequenceRNN to predict logits[g]: [T, N_g, K].
    We feed the full past events sequence for each node and get logits
    for t=1..T-1; we leave t=0 as zeros.
    """
    model.eval()
    T = int(meta["T"])
    K = 2
    logits_dict: Dict[int, np.ndarray] = {}

    with torch.no_grad():
        for g, comm in enumerate(communities):
            X_g = comm["X"].astype(np.float32)    # [N_g, d_static]
            Y_g = comm["Y"].astype(np.float32)    # [T, N_g, K]
            T_g, N_g, _ = Y_g.shape
            assert T_g == T
            d_static = X_g.shape[1]

            logits_g = np.zeros((T, N_g, K), dtype=np.float32)

            for i in range(N_g):
                x_i = X_g[i]
                past_events = Y_g[:-1, i, :]  # [T-1, K]
                x_rep = np.repeat(x_i[None, :], repeats=past_events.shape[0], axis=0)
                X_i_seq = np.concatenate([past_events, x_rep], axis=1)  # [T-1, K + d_static]

                X_i_torch = torch.from_numpy(X_i_seq[None, ...]).to(device)  # [1, T-1, d_in]
                logits_i = model(X_i_torch).cpu().numpy()[0]  # [T-1, K]

                logits_g[1:, i, :] = logits_i

            logits_dict[g] = logits_g

    return logits_dict


