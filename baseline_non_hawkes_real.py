# Generic panel builder for (t, node) -> features and labels
# ----------------------------------------------------------------------

def build_panel_data(
    communities: List[dict],
    meta: Dict,
    mode: Literal["rum", "spatial_lag", "recovus"],
    k_neighbors: int = 5,
) -> Tuple[np.ndarray, np.ndarray, np.ndarray, List[Tuple[int, int]]]:
    """
    Build panel data for discrete-time logistic baselines.

    We construct samples of the form:
        X_panel[n, :]  - features for one (t, i) pair
        Y_panel[n, k]  - binary label for sell/repair at time t
        time_idx[n]    - time index t (0-based)
        index_map[n]   - (g, i) -> community index and node index

    For training, you will typically restrict to t < T_train.
    For prediction, you rebuild features for all t and use fitted model.

    mode:
      - "rum": random utility / logit with:
            [X_i, t/T, t^2/T^2]
      - "spatial_lag": plus spatial lag of last-period events via kNN W
      - "recovus": plus community functionality indices
            (share_repaired, share_sold in the community at t-1)
    """
    T = int(meta["T"])
    T_train = int(meta["T_train"])
    K = 2  # sell, repair

    X_list = []
    Y_list = []
    t_list = []
    idx_list = []

    for g, comm in enumerate(communities):
        X_g = comm["X"].astype(np.float32)  # [N_g, d]
        Y_g = comm["Y"].astype(np.float32)  # [T, N_g, K]
        coords_g = comm["coords"].astype(np.float32)  # [N_g, 2]
        T_g, N_g, _ = Y_g.shape
        assert T_g == T, "Inconsistent T across communities"

        # Precompute W_g if needed
        W_g = None
        if mode == "spatial_lag":
            W_g = build_knn_weights(coords_g, k=k_neighbors)

        for t in range(1, T_train):  # use t>=1 so we can look at t-1
            # Current-period labels (exact events)
            Y_t = Y_g[t]  # [N_g, K]
            # Last-period events
            Y_prev = Y_g[t - 1]  # [N_g, K]

            # Time features (normalized)
            t_norm = t / float(T)
            time_feats = np.array([t_norm, t_norm ** 2], dtype=np.float32)

            # Community-level functionality (for RecovUS mode)
            if mode == "recovus":
                # Fraction repaired & sold at t-1 in this CBG
                frac_sold_prev = float(Y_prev[:, 0].sum()) / max(N_g, 1)
                frac_rep_prev = float(Y_prev[:, 1].sum()) / max(N_g, 1)
                comm_feats = np.array(
                    [frac_sold_prev, frac_rep_prev, 1.0 - frac_sold_prev],
                    dtype=np.float32,
                )  # last term ~ "remaining occupancy"
            else:
                comm_feats = np.zeros(0, dtype=np.float32)

            # Spatial lags (for spatial_lag mode)
            if mode == "spatial_lag":
                # spatial lag of last-period sell/repair
                lag_sell = W_g @ Y_prev[:, 0]  # [N_g]
                lag_rep = W_g @ Y_prev[:, 1]   # [N_g]
                spatial_feats = np.stack([lag_sell, lag_rep], axis=1)  # [N_g, 2]
            else:
                spatial_feats = None

            for i in range(N_g):
                base = X_g[i]  # [d]
                feats = [base, time_feats]

                if mode == "recovus":
                    feats.append(comm_feats)
                if mode == "spatial_lag":
                    feats.append(spatial_feats[i])

                x_i = np.concatenate(feats, axis=0)  # 1D feature vector

                X_list.append(x_i)
                Y_list.append(Y_t[i])  # [K]
                t_list.append(t)
                idx_list.append((g, i))

    X_panel = np.stack(X_list, axis=0).astype(np.float32)  # [N_samples, d']
    Y_panel = np.stack(Y_list, axis=0).astype(np.float32)  # [N_samples, K]
    t_panel = np.array(t_list, dtype=np.int64)
    idx_map = idx_list  # list of (g, i)

    return X_panel, Y_panel, t_panel, idx_map


# ----------------------------------------------------------------------
# Simple multi-label logistic model (for RUM, spatial lag, RecovUS)
# ----------------------------------------------------------------------

class PanelLogitBaseline(nn.Module):
    def __init__(self, d_in: int, K: int = 2, hidden_dim: int = 0):
        super().__init__()
        if hidden_dim > 0:
            self.net = nn.Sequential(
                nn.Linear(d_in, hidden_dim),
                nn.ReLU(),
                nn.Linear(hidden_dim, K),
            )
        else:
            self.net = nn.Linear(d_in, K)

    def forward(self, x: torch.Tensor) -> torch.Tensor:
        # x: [N, d_in]
        return self.net(x)  # [N, K]


def train_panel_logit(
    X_train: np.ndarray,
    Y_train: np.ndarray,
    num_epochs: int = 20,
    lr: float = 1e-3,
    hidden_dim: int = 0,
    device: str = "cpu",
) -> PanelLogitBaseline:
    """
    Train a simple multi-label logistic model with BCEWithLogitsLoss.
    """
    K = Y_train.shape[1]
    d_in = X_train.shape[1]

    X_t = torch.from_numpy(X_train).to(device)
    Y_t = torch.from_numpy(Y_train).to(device)

    # imbalance-aware pos_weight
    flat = Y_train.reshape(-1, K)
    pos = flat.sum(axis=0).astype(float)
    neg = flat.shape[0] - pos
    pos[pos <= 0.0] = 1.0
    neg[neg <= 0.0] = 1.0
    ratio = neg / pos
    pos_weight = torch.from_numpy(np.sqrt(ratio)).float().to(device)

    model = PanelLogitBaseline(d_in=d_in, K=K, hidden_dim=hidden_dim).to(device)
    optimizer = torch.optim.Adam(model.parameters(), lr=lr)
    loss_fn = nn.BCEWithLogitsLoss(pos_weight=pos_weight)

    for epoch in range(1, num_epochs + 1):
        model.train()
        optimizer.zero_grad()
        logits = model(X_t)
        loss = loss_fn(logits, Y_t)
        loss.backward()
        optimizer.step()

        if epoch == 1 or epoch % 5 == 0 or epoch == num_epochs:
            print(f"[PanelLogit epoch {epoch:03d}] loss={loss.item():.4f}")

    return model


def predict_panel_logit_logits(
    model: PanelLogitBaseline,
    communities: List[dict],
    meta: Dict,
    mode: Literal["rum", "spatial_lag", "recovus"],
    k_neighbors: int = 5,
    device: str = "cpu",
) -> Dict[int, np.ndarray]:
    """
    Use a trained PanelLogitBaseline to produce logits_dict[g]:
        logits[g]: [T, N_g, K] for exact events.
    We rebuild the same type of features but over the full horizon 1..T-1,
    and set logits[0] = 0.
    """
    model.eval()
    T = int(meta["T"])
    K = 2
    logits_dict: Dict[int, np.ndarray] = {}

    with torch.no_grad():
        for g, comm in enumerate(communities):
            X_g = comm["X"].astype(np.float32)  # [N_g, d0]
            Y_g = comm["Y"].astype(np.float32)  # [T, N_g, K]
            coords_g = comm["coords"].astype(np.float32)
            T_g, N_g, _ = Y_g.shape
            assert T_g == T

            # Precompute W_g if needed
            W_g = None
            if mode == "spatial_lag":
                W_g = build_knn_weights(coords_g, k=k_neighbors)

            logits_g = np.zeros((T, N_g, K), dtype=np.float32)

            for t in range(1, T):
                Y_prev = Y_g[t - 1]
                t_norm = t / float(T)
                time_feats = np.array([t_norm, t_norm ** 2], dtype=np.float32)

                if mode == "recovus":
                    frac_sold_prev = float(Y_prev[:, 0].sum()) / max(N_g, 1)
                    frac_rep_prev = float(Y_prev[:, 1].sum()) / max(N_g, 1)
                    comm_feats = np.array(
                        [frac_sold_prev, frac_rep_prev, 1.0 - frac_sold_prev],
                        dtype=np.float32,
                    )
                else:
                    comm_feats = np.zeros(0, dtype=np.float32)

                if mode == "spatial_lag":
                    lag_sell = W_g @ Y_prev[:, 0]
                    lag_rep = W_g @ Y_prev[:, 1]
                    spatial_feats = np.stack([lag_sell, lag_rep], axis=1)
                else:
                    spatial_feats = None

                X_list = []
                for i in range(N_g):
                    base = X_g[i]
                    feats = [base, time_feats]
                    if mode == "recovus":
                        feats.append(comm_feats)
                    if mode == "spatial_lag":
                        feats.append(spatial_feats[i])
                    X_list.append(np.concatenate(feats, axis=0))

                X_t = np.stack(X_list, axis=0).astype(np.float32)  # [N_g, d_in]
                X_t_torch = torch.from_numpy(X_t).to(device)
                logits_t = model(X_t_torch).cpu().numpy()  # [N_g, K]
                logits_g[t] = logits_t

            logits_dict[g] = logits_g

    return logits_dict


